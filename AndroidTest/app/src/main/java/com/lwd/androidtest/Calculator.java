public class Calculator {

    /**
     * 求和
     * @param arg0
     * @param arg1
     * @return
     */
    public double summation(double arg0,double arg1){
        return arg0+arg1;
    }

    /**
     * 求减
     * @param arg0
     * @param arg1
     * @return
     */
    public double subtraction(double arg0,double arg1){
        return arg0-arg1;
    }

    /**
     * 求乘
     * @param arg0
     * @param arg1
     * @return
     */
    public double multiplication(double arg0,double arg1){
        return arg0*arg1;
    }

    /**
     * 求除
     * @param arg0
     * @param arg1
     * @return
     */
    public double division(double arg0,double arg1){
        return arg0/arg1;
    }


}
